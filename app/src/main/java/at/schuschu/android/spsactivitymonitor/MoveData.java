public class MoveData {
    public float x;
    public float y;
    public float z;
    public float phi;
    public float theta;

    public MoveData(float x, float y, float z, float phi, float theta) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.phi = phi;
        this.theta = theta;
    }
}
